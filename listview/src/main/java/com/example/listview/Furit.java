package com.example.listview;

public class Furit {
    private String name;
    private int imageId;

    public Furit(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
